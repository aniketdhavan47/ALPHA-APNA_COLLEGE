package com.module13;

public class BitwiseOpreators {
	public static void main(String[] args) {
		//And = &
		System.out.println(5 & 6);
		// OR |
		System.out.println(5 | 6);
		// XOR ^
		System.out.println(5 ^ 6);
		// 1's Complement ~
		System.out.println( ~ 5);
		//binary LeftShift
		System.out.println(5<<2);
		//a<<b=a*2^b;
		//Binary RightShift
		System.out.println(5>>2);
		//a>>b=a/2^b
	}

}
